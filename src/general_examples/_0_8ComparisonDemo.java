package general_examples;

//==      equal to
//!=      not equal to
//>       greater than
//>=      greater than or equal to
//<       less than
//<=      less than or equal to
public class _0_8ComparisonDemo {
     public static void main(String[] args){
        int value1 = 1;
        int value2 = 2;
        if(value1 == value2)
            System.out.println("value1 == value2");
        if(value1 != value2)
            System.out.println("value1 != value2");
        if(value1 > value2)
            System.out.println("value1 > value2");
        if(value1 < value2)
            System.out.println("value1 < value2");
        if(value1 <= value2)
            System.out.println("value1 <= value2");
    }
}
